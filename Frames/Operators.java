package Sample;


public class Operators {
public static void main (String args[]) {
	System.out.println(3 >= 3 && 4>2);
	
	//&& 
	//this operator checks whether both of the conditions are true.
	//if any of them is false then it will eeturn false else true
	System.out.println(3<3 || 4<2);
	//||
	//this operator checks whether any of the conditions are true.
	//if any of them is true then it will eeturn true else else
}
}
